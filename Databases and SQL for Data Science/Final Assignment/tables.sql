CREATE DATABASE CHICAGO;

USE CHICAGO;

-- DROP TABLE CENSUS;
CREATE TABLE CENSUS(
					COMMUNITY_AREA_NUMBER INT NOT NULL, 
					COMMUNITY_AREA_NAME VARCHAR(25),
					PERCENT_OF_HOUSING_CROWDED DECIMAL(10,2),
					PERCENT_HOUSEHOLDS_BELOW_POVERTY DECIMAL(10,2),
                    PERCENT_AGED_OVER_16_UNEMPLOYED DECIMAL(10,2),
                    PERCENT_AGED_OVER_25_WITHOUT_HIGH_SCHOOL_DIPLOMA DECIMAL(10,2),
                    PERCENT_AGED_UNDER_18_OR_OVER_64 DECIMAL(10,2),
                    PER_CAPITA_INCOME INT,
                    HARDSHIP_INDEX INT,
					PRIMARY KEY (COMMUNITY_AREA_NUMBER));
                    
LOAD DATA INFILE 'Census_Data_Chicago.csv' 
INTO TABLE CENSUS
FIELDS TERMINATED BY ',' ENCLOSED BY '"'
LINES TERMINATED BY '\n'
IGNORE 1 ROWS;

