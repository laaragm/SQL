				ID BIGINT NOT NULL,
                CASE_NUMBER VARCHAR(20),
                DATE VARCHAR(40),
                BLOCK VARCHAR(40),
                IUCR INT,
                PRIMARY_TYPE VARCHAR(30),
                DESCRIPTION VARCHAR(20),
                LOCATION_DESCRIPTION VARCHAR(40),
                ARREST VARCHAR(25),
                DOMESTIC VARCHAR(15),
                BEAT VARCHAR(15),
                DISTRICT INT,
                WARD INT,
                COMMUNITY_AREA_NUMBER INT,
                FBICODE INT,
                X_COORDINATE BIGINT,
                Y_COORDINATE BIGINT,
                YEAR INT,
                UPDATEDON VARCHAR(30),
